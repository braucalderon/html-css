class Player():

    """ Creates a single player class that handle multiple players
        and counts at the same time """
 
    def __init__(self,name):

        self.name = name
        self.points = 2     

    def set_value(self):

        self.points += 2

    def set_name(self):

        self.name = name
        
    def get_value(self):

        return self.points 

    def get_name(self):

        return self.name
        
    

        
        
        
        

        
    

    
